# 9 Payment System
class Payment(ABC):
    @abstractmethod
    def pay(self, amount):
        pass

class CreditCardPayment(Payment):
    def __init__(self, card_number):
        self.__card_number = card_number
    def pay(self, amount):
        print(f"Paid {amount} via Credit Card ending in {str(self.__card_number)[-4:]}")

class CashPayment(Payment):
    def pay(self, amount):
        print(f"Paid {amount} in Cash")

print("\n--- Payment System ---")
CreditCardPayment(1234567890123456).pay(5000)
CashPayment().pay(1000)



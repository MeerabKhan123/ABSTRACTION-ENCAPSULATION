# 8 Abstract Employee (Salary)
class EmployeeABC(ABC):
    @abstractmethod
    def calculate_salary(self):
        pass

class FullTimeEmployee(EmployeeABC):
    def __init__(self, monthly):
        self.monthly = monthly
    def calculate_salary(self):
        print(f"Full-Time Salary: {self.monthly} per month")

class PartTimeEmployee(EmployeeABC):
    def __init__(self, hours, rate):
        self.hours = hours
        self.rate = rate
    def calculate_salary(self):
        print(f"Part-Time Salary: {self.hours * self.rate} (hours x rate)")

print("\n--- Abstract Employee ---")
FullTimeEmployee(60000).calculate_salary()
PartTimeEmployee(80, 200).calculate_salary()


